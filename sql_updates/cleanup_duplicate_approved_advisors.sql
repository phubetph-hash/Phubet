-- Clean up duplicate approved advisors per student (business rule violation).
-- Keep only the most recent approved request per student, cancel others.

UPDATE request r1
JOIN (
  SELECT student_id, MAX(request_id) AS latest_request_id
  FROM request
  WHERE status = 0xE0B8ADE0B899E0B8B8E0B8A1E0B8B1E0B895E0B8B4
  GROUP BY student_id
) r2 ON r1.student_id = r2.student_id
SET r1.status = 0xE0B8A2E0B881E0B980E0B8A5E0B8B4E0B881,
    r1.rejection_reason = 0xE0B8A3E0B8B0E0B89AE0B89AE0B8A2E0B881E0B980E0B8A5E0B8B4E0B881E0B8ADE0B8B1E0B895E0B982E0B899E0B8A1E0B8B1E0B895E0B8B43A20E0B899E0B8B4E0B8AAE0B8B4E0B895E0B8A1E0B8B5E0B8ADE0B8B2E0B888E0B8B2E0B8A3E0B8A2E0B98CE0B897E0B8B5E0B988E0B8ADE0B899E0B8B8E0B8A1E0B8B1E0B895E0B8B4E0B981E0B8A5E0B989E0B8A7,
    r1.suggestion = 0xE0B894E0B8B3E0B980E0B899E0B8B4E0B899E0B881E0B8B2E0B8A3E0B881E0B8B1E0B89AE0B8ADE0B8B2E0B888E0B8B2E0B8A3E0B8A2E0B98CE0B981E0B8A5E0B989E0B8A7
WHERE r1.status = 0xE0B8ADE0B899E0B8B8E0B8A1E0B8B1E0B895E0B8B4
  AND r1.request_id <> r2.latest_request_id;
